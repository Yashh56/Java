public class Primitives {
    public static void main(String[] args) {
        int rollno = 20;
        char letter = 'a';
        float num = 29.12f; // f is used to tell compiler that it is a float value
        double largeDecimalNumbers = 123.123456789; // double is used for large decimal numbers
        boolean check = true;
        long largeInteger = 1234567890328727L; // L is used to tell compiler that it is a long value

        System.out.println(rollno);
        System.out.println(letter);
        System.out.println(num);
        System.out.println(largeDecimalNumbers);
        System.out.println(check);
        System.out.println(largeInteger);
    }
}
